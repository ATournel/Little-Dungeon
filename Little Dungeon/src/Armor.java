
public class Armor {
	
	String name;
	int armor;
	int magicArmor;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getArmor() {
		return armor;
	}
	public void setArmor(int armor) {
		this.armor = armor;
	}
	public int getMagicArmor() {
		return magicArmor;
	}
	public void setMagicArmor(int magicArmor) {
		this.magicArmor = magicArmor;
	}

}
